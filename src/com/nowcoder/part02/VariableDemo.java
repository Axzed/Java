package com.nowcoder.part02;

/*
* 这是一个关于变量的实例
* 该示例演示变量操作
* */
public class VariableDemo {

    public static void main(String[] args) {
        // 1.变量声明
        int a;
        int b, c;

        // 2.变量的初始化
        a = 10;
        b = 20;
        int d = 30;

        // 3.变量的使用
        // System.out.println(n);
        // System.out.println(c);
        // a = 3.14;
        System.out.println(a);
        System.out.println(b);
        a = 1;
        b = 2;
        System.out.println(a + b);
        System.out.println(a * b);

        // 4.变量的命名
        // int #;
        // int 2n;
        // int class;
        // 驼峰式命名规则
        int score;
        int javaScore;
        int myJavaScore;

    }

}
